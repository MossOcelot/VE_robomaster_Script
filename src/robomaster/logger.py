def info(msg: str) -> None:
    """Log an info message."""
    print(f"[INFO] {msg}")

def warning(msg: str) -> None:
    """Log a warning message."""
    print(f"[WARNING] {msg}")

def error(msg: str) -> None:
    """Log an error message."""
    print(f"[ERROR] {msg}")

def debug(msg: str) -> None:
    """Log a debug message."""
    print(f"[DEBUG] {msg}")